import React from 'react';
import styles from './VideoQuestionsDisplay.module.css';
import { HelpCircle, ChevronRight } from 'lucide-react';

interface VideoQuestionsDisplayProps {
    questions: string[];
}

export default function VideoQuestionsDisplay({ questions }: VideoQuestionsDisplayProps) {
    if (!questions || questions.length === 0) return null;

    return (
        <div className={styles.container}>
            <div className={styles.header}>
                <HelpCircle size={24} className={styles.icon} />
                <h3 className={styles.title}>Generated Video Assessment Questions</h3>
            </div>
            <div className={styles.questionsList}>
                {questions.map((question, index) => (
                    <div key={index} className={styles.questionItem}>
                        <div className={styles.questionNumber}>
                            <span>{index + 1}</span>
                        </div>
                        <p className={styles.questionText}>{question}</p>
                        <ChevronRight size={18} className={styles.arrow} />
                    </div>
                ))}
            </div>
            <p className={styles.footerNote}>
                These questions were generated by AI to evaluate student's conceptual understanding and communication skills.
            </p>
        </div>
    );
}
